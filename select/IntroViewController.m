#import "IntroViewController.h"

@interface IntroViewController ()

@property (weak, nonatomic) IBOutlet UISwitch *switcher;

@end

@implementation IntroViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    _pageTitles = @[@"This app is a fast way to see any data from MS SQL Server database. It can be useful, when you need to control something in your organization while you are out of your workplace. Let's see how it works!",
                    @"For example, you have the table with transactions of your organization's account.",
                    @"You can write a 'short' SQL query, which returns 1 row, to get daily totals and see them on the main screen. Call it anyway you like, 'Transactions', for example.",
                    @"From the main screen you can move to the detailed screen by touching one of  short queries. All you need is to write an another SQL query, which returns all transactions."];
    _pageImages = @[@"", @"page1.png", @"page2.png", @"page3.png"];
    
    // Create page view controller
    self.pageViewController = [self.storyboard instantiateViewControllerWithIdentifier:@"PageViewController"];
    self.pageViewController.dataSource = self;
    
    PageContentViewController *startingViewController = [self viewControllerAtIndex:0];
    NSArray *viewControllers = @[startingViewController];
    [self.pageViewController setViewControllers:viewControllers direction:UIPageViewControllerNavigationDirectionForward animated:NO completion:nil];
    
    // Change the size of page view controller
    self.pageViewController.view.frame = CGRectMake(0, 0, self.view.frame.size.width, self.view.frame.size.height - 30);
    
    [self addChildViewController:_pageViewController];
    [self.view addSubview:_pageViewController.view];
    [self.pageViewController didMoveToParentViewController:self];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

